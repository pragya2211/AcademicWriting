<h1>FAQs</h1>
<h3>Can I seek individual writing consultation?</h3>
<p align='justify' style='font-size:14px;'>If you are writing your research paper and you need some help in it, you might feel free to
request for a slot on Individual writing consultations. We will arrange for a one-on-one
meeting. </p>

<h3>What do I have to bring if I may an appointment for individual writing consultation?</h3>
<p align='justify' style='font-size:14px;'>It will be always better if you bring the draft you are currently working on. Please do not
just come with a basic outline of notes or without anything. </p>

<h3>Why types of writing do we not handle?</h3>
<p align='justify' style='font-size:14px;'>If you need help with your resume/cv, and other non-academic career materials, regular
course assignments, AWL won’t be doing that. </p>

<h3>Where do individual guidance/consultations take place?</h3>
<p align='justify' style='font-size:14px;'>In the Writing Lab itself, A-219. </p>

<h3>Do you offer online consultations?</h3>
<p align='justify' style='font-size:14px;'>Yes, if necessary. </p>

<h3>How do I cancel a meeting?</h3>
<p align='justify' style='font-size:14px;'>Just drop an email if you need to cancel your consultation
an hour earlier.<br>
Kindly refrain from unexplained absences at appointments or frivolous cancelations. </p>

<h3>Does the AWL provide copy-editing or proofreading services?</h3>
<p align='justify' style='font-size:14px;'>No, the AWL does not provide copy-editing or proofreading services. The Lab’s objective is
to help graduate students grow as independent academic writers, rather than proofread for

them. If you are interested in improving your editing and proofreading skills, please seek
help somewhere else. </p>


